name = "\tJotaro Kujo\n" #the variable "name" is assigned the string on this line

print("Unmodified:") 
print(name) #this code prints the name normally

print("\nUsing lstrip():")
print(name.lstrip()) #this code prints the name while removing the leading characters

print("\nUsing rstrip():")
print(name.rstrip()) #this code prints the name while removing the trailing characters

print("\nUsing strip():")
print(name.strip()) #this code prints the name while removing the leading and trailing characters